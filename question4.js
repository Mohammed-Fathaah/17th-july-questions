let vehicleType = "b";

switch (vehicleType) {
    case "c":
        console.log("Car");
        break;
    case "b":
        console.log("Bike");
        break;
    case "t":
        console.log("Truck");
        break;
    case "a":
        console.log("Auto");
        break;
    default:
        console.log("Unknown Vehicle");
}